package model;

/**
 *
 * @author tuana
 */
public class Stock {
    private int userID;
    private int foodID;
    private int quantity;

    public Stock(int userID,int foodID, int quantity) {
        this.userID= userID;
        this.foodID = foodID;
        this.quantity = quantity;
    }

    public Stock() {
    }

    public int getFoodID() {
        return foodID;
    }

    public void setFoodID(int foodID) {
        this.foodID = foodID;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public int getUserID() {
        return userID;
    }

    public void setUserID(int userID) {
        this.userID = userID;
    }
    
    
    
    
}
